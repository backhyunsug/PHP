<div style='color:blue;font-size:12pt'>PHP Web Programming</div>
div 태그를 이용해서 글자에 효과를 줄 수 있다. div 태그에 있는 style속성에 글자색은 파란색으로 
글자의 크기는 12pt를 주었다 for문을 이용해 글씨의 크기를 12pt부터 24pt까지 키워보도록 합시다 
php 는 문자열과 문자열을 결합할때 .(dot) 연산자를 사용합니다. 
 문자열과 정수 변수를 더하면 정수 타입이 문자열 타입으로 전환되어 사용됩니다 

